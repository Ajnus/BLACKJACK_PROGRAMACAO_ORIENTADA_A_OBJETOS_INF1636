package model;

public class Ficha {
	private int id;
	private int valor;
	private String cor;

	public void setId(int id) {
		this.id = id;
	}

	public int getId() {
		return id;
	}

	public void setValor(int valor) {
		this.valor = valor;
	}

	public int getValor() {
		return valor;
	}

	public void setCor(String cor) {
		this.cor = cor;
	}

	public String getCor() {
		return cor;
	}

}
